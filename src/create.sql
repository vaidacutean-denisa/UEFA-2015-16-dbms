    ID_TARA                 NUMBER UNIQUE,
    NUME                    VARCHAR2(50) NOT NULL,
    DATA_INCEPUT            DATE NOT NULL,
    DATA_SFARSIT            DATE NOT NULL,
    
    CONSTRAINT FK_CMI_ASOCIATIE FOREIGN KEY (ID_ASOCIATIE)
        REFERENCES ASOCIATIE(ID_ASOCIATIE),
    
    CONSTRAINT FK_CMI_TARA FOREIGN KEY (ID_TARA)
        REFERENCES TARA(ID_TARA),

    CONSTRAINT CK_DATE_INTERN CHECK (DATA_SFARSIT > DATA_INCEPUT),

    CONSTRAINT PK_CM_INTERN PRIMARY KEY (ID_CAMPIONAT)
);

CREATE TABLE ETAPA_CAMPIONAT (
    ID_CAMPIONAT            NUMBER,
    NUMAR_ETAPA             NUMBER,
    DATA_INCEPUT            DATE NOT NULL,
    DATA_SFARSIT            DATE NOT NULL,
    
    CONSTRAINT FK_ETAPA_CAMP FOREIGN KEY (ID_CAMPIONAT)
        REFERENCES CAMPIONAT_INTERN(ID_CAMPIONAT),
        
    CONSTRAINT CK_DATE_ETAPA CHECK (DATA_SFARSIT > DATA_INCEPUT),

    CONSTRAINT PK_ETAPA_CM PRIMARY KEY (ID_CAMPIONAT, NUMAR_ETAPA)
