    CONSTRAINT CK_DATE_EURO CHECK (DATA_SFARSIT > DATA_INCEPUT),
        
    CONSTRAINT PK_CME PRIMARY KEY (ID_EDITIE_EURO)
);


CREATE TABLE NATIONALA (
    ID_NATIONALA            NUMBER DEFAULT NATIONALA_SEQ.NEXTVAL,
    ID_EDITIE_EURO          NUMBER,
    ID_TARA                 NUMBER NOT NULL UNIQUE, 
    NUME                    VARCHAR2(50),
    SELECTIONER             VARCHAR2(50) NOT NULL,
