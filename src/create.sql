    CONSTRAINT FK_NATIONALA_EURO FOREIGN KEY (ID_EDITIE_EURO)
        REFERENCES CAMPIONAT_EUROPEAN(ID_EDITIE_EURO),
        
    CONSTRAINT FK_NAT_TARA FOREIGN KEY (ID_TARA)
        REFERENCES TARA (ID_TARA),
        
    CONSTRAINT PK_NATIONALA PRIMARY KEY (ID_NATIONALA)
);


CREATE TABLE CAMPIONAT_INTERN (
    ID_CAMPIONAT            NUMBER DEFAULT CM_INTERN_SEQ.NEXTVAL,
