CREATE TABLE COMPETITIE_EURO_CLUB (
    ID_COMPETITIE           NUMBER,
    NUME_COMPETITIE         VARCHAR2(30) NOT NULL,
    DATA_INCEPUT            DATE NOT NULL,
    DATA_SFARSIT            DATE NOT NULL,
    NUMAR_ECHIPE            NUMBER NOT NULL,
    
    CONSTRAINT CK_DATE_CLUB CHECK (DATA_SFARSIT > DATA_INCEPUT),

    CONSTRAINT CK_NR_ECHIPE CHECK (NUMAR_ECHIPE BETWEEN 4 AND 99),

    CONSTRAINT PK_CLUB_EU PRIMARY KEY (ID_COMPETITIE)
);


CREATE TABLE FAZA_COMPETITIE (
    ID_FAZA_COMPETITIE      NUMBER,
    ID_COMPETITIE           NUMBER,
    NUME_FAZA               VARCHAR2(30) NOT NULL,
    DATA_INCEPUT            DATE NOT NULL,
    DATA_SFARSIT            DATE NOT NULL,
    
    CONSTRAINT CK_FAZA_DATE CHECK (DATA_INCEPUT < DATA_SFARSIT),
    
    CONSTRAINT FK_FAZA_EDITIE FOREIGN KEY (ID_COMPETITIE)
        REFERENCES COMPETITIE_EURO_CLUB (ID_COMPETITIE),
    
    CONSTRAINT PK_FAZA PRIMARY KEY (ID_COMPETITIE, ID_FAZA_COMPETITIE)
);
    

CREATE TABLE STADION (
    ID_STADION              NUMBER DEFAULT STADION_SEQ.NEXTVAL,
    ID_ORAS                 NUMBER NOT NULL,
    NUME                    VARCHAR2(50) NOT NULL,
    CAPACITATE              NUMBER NOT NULL,
    
    CONSTRAINT CK_STADION_CAPACITATE CHECK (CAPACITATE BETWEEN 10000 AND 300000),
    
    CONSTRAINT FK_ORAS FOREIGN KEY (ID_ORAS)
        REFERENCES ORAS (ID_ORAS),
    
    CONSTRAINT PK_STADION PRIMARY KEY (ID_STADION)
);
    

CREATE TABLE ECHIPA (
    ID_ECHIPA               VARCHAR2(3)
        CONSTRAINT CK_ID_ECHIPA CHECK (LENGTH(ID_ECHIPA) = 3),
    
    ID_CAMPIONAT            NUMBER NOT NULL,
    ID_COMPETITIE           NUMBER,
    ID_STADION              NUMBER NOT NULL,
    TIP_STADION             VARCHAR2(20) NOT NULL,
    NUME                    VARCHAR2(50) NOT NULL,
    
    CONSTRAINT FK_EC_INTERN FOREIGN KEY (ID_CAMPIONAT)
        REFERENCES CAMPIONAT_INTERN(ID_CAMPIONAT),
        
    CONSTRAINT FK_EC_EURO FOREIGN KEY (ID_COMPETITIE)
        REFERENCES COMPETITIE_EURO_CLUB(ID_COMPETITIE),
        
    CONSTRAINT FK_EC_STADION FOREIGN KEY (ID_STADION)
        REFERENCES STADION (ID_STADION),
    
    CONSTRAINT CK_TIP_STADION CHECK (TIP_STADION IN ('PROPRIU', 'INCHIRIAT')),

    CONSTRAINT PK_ECHIPA PRIMARY KEY (ID_ECHIPA)
);


-- doua chei straine catre ECHIPA, pentru a garanta ca intr-un meci joaca exact doua echipe
CREATE TABLE MECI (
    ID_MECI                 NUMBER DEFAULT MECI_SEQ.NEXTVAL,
    ID_STADION              NUMBER NOT NULL,
    DATA_MECI               DATE NOT NULL,
    
    ID_ECHIPA_GAZDA         VARCHAR2(3) NOT NULL,
    ID_ECHIPA_OASPETE       VARCHAR2(3) NOT NULL,   
    GOLURI_GAZDA            NUMBER DEFAULT 0,                   -- idee de trigger: dupa insert, actualizez coloanele in functie de echipa marcatorului
    GOLURI_OASPETE          NUMBER DEFAULT 0,
    
    CONSTRAINT FK_MECI_STADION FOREIGN KEY (ID_STADION)
        REFERENCES STADION(ID_STADION),
    
    CONSTRAINT FK_MECI_GAZDA FOREIGN KEY (ID_ECHIPA_GAZDA)
        REFERENCES ECHIPA(ID_ECHIPA),
        
    CONSTRAINT FK_MECI_OASPETE FOREIGN KEY (ID_ECHIPA_OASPETE)
        REFERENCES ECHIPA(ID_ECHIPA),
    
    CONSTRAINT CK_GOLURI CHECK (GOLURI_GAZDA >= 0 AND GOLURI_OASPETE >= 0),
    
    CONSTRAINT CK_ECHIPE_DISTINCTE CHECK (ID_ECHIPA_GAZDA <> ID_ECHIPA_OASPETE),
    
    -- pentru a garanta ca nu se pot desfasura mai multe meciuri pe acelasi stadion, in aceeasi zi
    CONSTRAINT UQ_STADION_ZI UNIQUE (ID_STADION, DATA_MECI),
    
    CONSTRAINT PK_MECI PRIMARY KEY (ID_MECI)
);

