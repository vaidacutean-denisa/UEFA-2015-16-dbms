CREATE TABLE SPONSORIZARE (
    ID_SPONSOR              NUMBER NOT NULL,
    ID_ECHIPA               VARCHAR2(3) NOT NULL,
    
    -- prin 'date' modelez doar anul
    DATA_INCEPUT            NUMBER(4)   
        CONSTRAINT CK_SZ_INCEPUT CHECK (DATA_INCEPUT BETWEEN 1950 AND 2030)
        NOT NULL,
        
    DATA_SFARSIT            NUMBER(4)
        CONSTRAINT CK_SZ_SFARSIT CHECK (DATA_SFARSIT BETWEEN 1950 AND 2030)
        NOT NULL,
        
    VALOARE_CONTRACT        NUMBER NOT NULL,
    
    CONSTRAINT FK_SZ_ECHIPA FOREIGN KEY (ID_ECHIPA)
        REFERENCES ECHIPA (ID_ECHIPA),
        
    CONSTRAINT FK_SZ_SPONSOR FOREIGN KEY (ID_SPONSOR)
        REFERENCES SPONSOR (ID_SPONSOR),
        
    CONSTRAINT CK_SPONSORIZARE_DATE CHECK (DATA_SFARSIT > DATA_INCEPUT),
    
    CONSTRAINT PK_SPONSORIZARE PRIMARY KEY (ID_SPONSOR, ID_ECHIPA, DATA_INCEPUT)
);


