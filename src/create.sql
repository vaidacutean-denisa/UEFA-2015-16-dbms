CREATE TABLE JUCATOR (
    ID_JUCATOR              NUMBER DEFAULT JUCATOR_SEQ.NEXTVAL,
    ID_ECHIPA               VARCHAR2(3) NOT NULL,
    ID_NATIONALA            NUMBER,
    NUME                    VARCHAR2(40) NOT NULL,              
    PRENUME                 VARCHAR2(40) NOT NULL,
    DATA_NASTERII           DATE NOT NULL,
    NATIONALITATE           VARCHAR2(20) NOT NULL,              -- nationala este null daca jucatorul nu este european; cum pot sa aflu nationalitatea?
    POZITIE                 VARCHAR2(50) NOT NULL,
    CONVOCAT                VARCHAR2(2) NOT NULL,

    CONSTRAINT FK_JUCATOR_ECHIPA FOREIGN KEY (ID_ECHIPA)
        REFERENCES ECHIPA(ID_ECHIPA),
        
    CONSTRAINT FK_JUCATOR_NATIONALA FOREIGN KEY (ID_NATIONALA)
        REFERENCES NATIONALA (ID_NATIONALA),

    CONSTRAINT CK_JUCATOR_CONVOCAT CHECK (CONVOCAT IN ('DA', 'NU')),
    
    -- jucatorul cu nationala poate fi sau nu convocat; jucatorul fara nationala (cazul non-european) nu poate fi convocat (constrangerea asigura ca nu am straini convocati)
    CONSTRAINT CK_J_NAT_CONVOCAT CHECK
        ((ID_NATIONALA IS NOT NULL AND CONVOCAT IN ('DA', 'NU'))
        OR
        (ID_NATIONALA IS NULL AND CONVOCAT = 'NU')),
    
    CONSTRAINT PK_JUCATOR PRIMARY KEY (ID_JUCATOR)
);


CREATE TABLE GOL (
    ID_MECI                 NUMBER,
