CREATE SEQUENCE TARA_SEQ START WITH 1;
CREATE SEQUENCE ORAS_SEQ START WITH 1;
CREATE SEQUENCE ASOCIATIE_SEQ START WITH 1;
CREATE SEQUENCE NATIONALA_SEQ START WITH 1;
CREATE SEQUENCE CM_INTERN_SEQ START WITH 1;
CREATE SEQUENCE STADION_SEQ START WITH 1;
CREATE SEQUENCE MECI_SEQ START WITH 1;
CREATE SEQUENCE JUCATOR_SEQ START WITH 1;
CREATE SEQUENCE ANTRENOR_SEQ START WITH 1;
CREATE SEQUENCE SPONSOR_SEQ START WITH 1;

CREATE TABLE TARA (
    ID_TARA                 NUMBER DEFAULT TARA_SEQ.NEXTVAL,
    NUME                    VARCHAR2(50) NOT NULL,
    COD_ISO                 VARCHAR2(3)
        CONSTRAINT CK_COD_ISO CHECK (LENGTH(COD_ISO) = 3)
        NOT NULL,
    
    CONSTRAINT PK_TARA PRIMARY KEY (ID_TARA)
);


CREATE TABLE ORAS (
    ID_ORAS                 NUMBER DEFAULT ORAS_SEQ.NEXTVAL,
    ID_TARA                 NUMBER NOT NULL,
    NUME                    VARCHAR2(50) NOT NULL,
    
    CONSTRAINT FK_ORAS_TARA FOREIGN KEY (ID_TARA)
        REFERENCES TARA (ID_TARA),
        
    CONSTRAINT PK_ORAS PRIMARY KEY (ID_ORAS)
);

      
CREATE TABLE ASOCIATIE (
    ID_ASOCIATIE            NUMBER DEFAULT ASOCIATIE_SEQ.NEXTVAL,
    NUME                    VARCHAR2(50) NOT NULL,
    SEDIU                   NUMBER NOT NULL,
    
    CONSTRAINT FK_AS_SEDIU FOREIGN KEY (SEDIU)
        REFERENCES ORAS(ID_ORAS),
    
    CONSTRAINT PK_ASOCIATIE PRIMARY KEY (ID_ASOCIATIE)
);



CREATE TABLE CAMPIONAT_EUROPEAN (
    ID_EDITIE_EURO          NUMBER,
